public class binary_And{
    public static void main(String args[]){
        System.out.println(5 & 6);//and
         System.out.println(5 | 6);//or
          System.out.println(5 ^ 6);//xar
           System.out.println(~5);//compliment
           System.out.println(5<<2);//binary left sift
           System.out.println(5>>2);//binary right sift
    }
}